    $(document).ready(function () {
        $('form').submit(function (event) {
            event.preventDefault();

            $.ajax({
                type: "POST",
                url: "/application/models/form-process.php",
                data:
                    $(this).serialize(),
                cache: false,
                processData: false,
                success: function (result){
                    if (result){
                        $("#msgSubmit").text("Не удалось отправить заявку!");
                        $("#msgSubmit").show('slow');
                        setTimeout(function() { $("#msgSubmit").hide('slow'); }, 3000);
                    }
                    else{
                        $("#msgSubmit").show('slow');
                        setTimeout(function() { $("#msgSubmit").hide('slow'); }, 3000);
                        $('input').val('');
                        $('textarea').val('');
                    }
                }
            });
        });
    });
